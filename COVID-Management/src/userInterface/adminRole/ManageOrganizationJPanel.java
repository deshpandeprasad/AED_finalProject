    public void populateOrganizationComboBox()
    {
        
      organizationCmb.removeAllItems();
    
            if(enterprise.getEnterpriseType().getValue().equals(EnterpriseType.Government.getValue()))
            {
             organizationCmb.addItem(Type.CovidOrganization);  
            }
            else if(enterprise.getEnterpriseType().getValue().equals(EnterpriseType.CoVolunteer.getValue()))
            {
            organizationCmb.addItem(Type.NurseSchool);   
            organizationCmb.addItem(Type.NurseSchoolHead);
           
            }
            else if(enterprise.getEnterpriseType().getValue().equals(EnterpriseType.Hospital.getValue()))
            {
            organizationCmb.addItem(Type.Doctor);  
            organizationCmb.addItem(Type.Patient);
            organizationCmb.addItem(Type.EmergencyDoctor);
            organizationCmb.addItem(Type.Ambulance);
            }
            else if(enterprise.getEnterpriseType().getValue().equals(EnterpriseType.Pharmacy.getValue()))
            {
                organizationCmb.addItem(Type.PharmacyHead);
            }
    
    }
    
    private void populateOrganizationTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : directory.getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrgId();
            row[1] = organization;
            
            model.addRow(row);
        }
    }
